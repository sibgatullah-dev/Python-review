# upper() method returns the string in upper case
a = "hello world"
print(a.upper())

# lower() method return the string in lower case
b = "HELLO WORLD"
print(b.lower())

# strip() mehod remove any white space from the beginning or the end 
c = " Hello world! "
print(c.strip())

# replace() method replaces a string with another string
d = "Hello World"
print(d.replace('H','J'))# Jello World

# split method returns a list where the text between the specified separator becomes the list item
e = "Hello,World"
print(e.split(','))

